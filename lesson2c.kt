fun main(args: Array<String>) {
    // the if....else if...else conditional statements
    // This is use to evaluate multiple conditions.  The else part get executed when all the conditions given evaluate to false

    val number = 10
    if( number > 0){
        println("the number is positive")
    }
    else if (number == 0){
        println("Number is zero")
    }
    else{
        println("The number is negative")
    }
}